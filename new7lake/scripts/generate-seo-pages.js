function applyOverrides(items, overrides) {
  items.forEach((item) => {
    const patch = overrides[item.slug];
    if (patch) Object.assign(item, patch);
  });
}

function applyObjectOverrides(target, overrides) {
  Object.entries(overrides).forEach(([key, patch]) => {
    if (target[key]) Object.assign(target[key], patch);
  });
}

