function depthFromSlug(slug) {
  if (!slug) return 0;
  return slug.split("/").length;
}

function prefix(slug) {
  return "../".repeat(depthFromSlug(slug));
}

function filePathFromSlug(slug) {
  return slug ? path.join(OUT, slug, "index.html") : path.join(OUT, "index.html");
}

function canonical(slug) {
  return slug ? `${SITE}/${slug}/` : `${SITE}/`;
}

function hrefFrom(slug, targetSlug) {
  const root = prefix(slug);
  if (!targetSlug) return `${root}index.html`;
  return `${root}${targetSlug}/index.html`;
}

function assetFrom(slug, assetPath) {
  return `${prefix(slug)}${assetPath}`;
}

function escapeHtml(text) {
  return String(text)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function localeOfSlug(slug) {
  return slug === "en" || String(slug).startsWith("en/") ? "en" : "ru";
}

function baseSlug(slug) {
  if (slug === "en") return "";
  return localeOfSlug(slug) === "en" ? String(slug).slice(3) : slug;
}

function localizedSlug(slug, locale) {
  if (!slug) return locale === "en" ? "en" : "";
  if (locale === "en") return localeOfSlug(slug) === "en" ? slug : `en/${slug}`;
  return baseSlug(slug);
}

function alternateSlug(slug) {
  return localeOfSlug(slug) === "en" ? baseSlug(slug) : localizedSlug(slug, "en");
}

function localeQuickLinks(slug) {
  return localeOfSlug(slug) === "en" ? quickLinksEn : quickLinks;
}

function getImageMeta(imagePath) {
  return imageMeta[imagePath] || { width: 1400, height: 933, sources: [{ path: imagePath, width: 1400 }] };
}

function renderPreloadImage(slug, imagePath) {
  const meta = getImageMeta(imagePath);
  const first = meta.sources?.[0] || { path: imagePath, width: meta.width };
  const srcset = (meta.sources || [{ path: imagePath, width: meta.width }])
    .map((source) => `${assetFrom(slug, source.path)} ${source.width}w`)
    .join(", ");
  return `<link
      rel="preload"
      as="image"
      href="${assetFrom(slug, first.path)}"
      imagesrcset="${srcset}"
      imagesizes="100vw"
      fetchpriority="high"
    />`;
}

function renderResponsiveImage(slug, imagePath, alt, options = {}) {
  const meta = getImageMeta(imagePath);
  const attrs = [
    `src="${assetFrom(slug, imagePath)}"`,
    meta.sources?.length
      ? `srcset="${meta.sources.map((source) => `${assetFrom(slug, source.path)} ${source.width}w`).join(", ")}"`
      : "",
    options.sizes ? `sizes="${options.sizes}"` : "",
    `width="${options.width || meta.width}"`,
    `height="${options.height || meta.height}"`,
    `alt="${escapeHtml(alt)}"`,
    options.loading ? `loading="${options.loading}"` : "",
    options.fetchpriority ? `fetchpriority="${options.fetchpriority}"` : "",
    `decoding="${options.decoding || "async"}"`
  ]
    .filter(Boolean)
    .join("\n              ");

  return `<img
              ${attrs}
            />`;
}

