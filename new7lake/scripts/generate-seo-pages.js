function write(slug, html) {
  const target = filePathFromSlug(slug);
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, html);
}

function renderSitemap(urls) {
  return `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9" xmlns:xhtml="http://www.w3.org/1999/xhtml">
${urls
  .map((url) => {
    if (url.alternates) {
      return `  <url>
    <loc>${url.loc}</loc>
    <xhtml:link rel="alternate" hreflang="ru" href="${url.alternates.ru}" />
    <xhtml:link rel="alternate" hreflang="en" href="${url.alternates.en}" />
    <xhtml:link rel="alternate" hreflang="x-default" href="${url.alternates.default}" />
    <lastmod>${DATE}</lastmod>
    <changefreq>${url.changefreq}</changefreq>
    <priority>${url.priority}</priority>
  </url>`;
    }
    return `  <url>
    <loc>${url.loc}</loc>
    <lastmod>${DATE}</lastmod>
    <changefreq>${url.changefreq}</changefreq>
    <priority>${url.priority}</priority>
  </url>`;
  })
  .join("\n")}
</urlset>`;
}

